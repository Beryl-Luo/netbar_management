package com.qlu.netbar_1.pojo;


public class Interim {

  private int interimId;
  private String interimPass;
  private int interimState;
  private int interimAccount;


  public int getInterimId() {
    return interimId;
  }

  public void setInterimId(int interimId) {
    this.interimId = interimId;
  }


  public String getInterimPass() {
    return interimPass;
  }

  public void setInterimPass(String interimPass) {
    this.interimPass = interimPass;
  }


  public int getInterimState() {
    return interimState;
  }

  public void setInterimState(int interimState) {
    this.interimState = interimState;
  }


  public int getInterimAccount() {
    return interimAccount;
  }

  public void setInterimAccount(int interimAccount) {
    this.interimAccount = interimAccount;
  }

}
